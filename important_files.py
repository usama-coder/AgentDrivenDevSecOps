# important_files.py

# List of important file extensions related to Python projects
IMPORTANT_EXTENSIONS = (
    ".py",  # Python source files
    ".json",  # JSON config files
    ".yaml", ".yml",  # YAML config files (e.g., CI/CD, Kubernetes)
    ".env",  # Environment variable files (e.g., storing secrets)
    ".ini",  # INI configuration files (commonly used in Python projects)
    ".cfg",  # Config files (e.g., setup.cfg for packaging)
    ".toml",  # TOML files (e.g., pyproject.toml for dependencies)
    "requirements.txt",  # Python dependency file
    "Pipfile",  # Alternative to requirements.txt
    "pyproject.toml",  # Modern Python package configuration
    "setup.py",  # Python package setup script
    "tox.ini",  # Tox configuration for testing

)
